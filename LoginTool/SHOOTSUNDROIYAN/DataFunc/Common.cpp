HANDLE hMapFile;

HANDLE CreateMapFile()
{
	hMapFile = CreateFileMapping( (HANDLE)0xFFFFFFFF, NULL,
							PAGE_READWRITE, 0, 
							0x800000, ID_SHARED_FILE );
	return hMapFile;
}

void ReadDataEx(LPSTR szData,DWORD dwOffsize,DWORD dwCopySize)
{
	LPSTR txt;
	if ( hMapFile )
	{
		// Make a View on the file mapping object
		// This makes it easier to handle the shared data 
		txt = (LPSTR)MapViewOfFile( hMapFile, FILE_MAP_ALL_ACCESS, 0, 0, 0);
		if (txt)
		{
			memcpy( szData, txt+dwOffsize,dwCopySize );
		}
	}
	UnmapViewOfFile(txt);
}

void WriteDataEx(LPSTR szData,DWORD dwOffsize,DWORD dwCopySize)
{
	LPSTR txt;
	if ( hMapFile )
	{
		// Make a View on the file mapping object
		// This makes it easier to handle the shared data 
		txt = (LPSTR)MapViewOfFile( hMapFile, FILE_MAP_ALL_ACCESS, 0, 0, 0);
		if (txt)
		{
			memcpy( txt+dwOffsize,szData,dwCopySize );
		}
	}
	UnmapViewOfFile(txt);
}

